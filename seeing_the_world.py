# list of places in the world I want to see.
visit_places = ['paris', 'switzerland', 'alaska', 'sydney']
print("Here is the original list.")
print(visit_places)

print("\nHere is the sorted list.")
print(sorted(visit_places))

print("\nHere is the original list again.")
print(visit_places)

print("\nReverse order of the original unsorted list.")
visit_places.reverse()
print(visit_places)

# permanent sorting list in a alphabetical order.
visit_places.sort()
print("\nHere is the alphabetically sorted list-- " + str(visit_places))

# Reverse order of sorted list.
visit_places.reverse()
print("\nHere is the reverse order of sorted list-- " + str(visit_places))




